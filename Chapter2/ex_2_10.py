import torch

t = torch.randn(2,3,4) # 2×3×4 のテンソルを作成
t
t[1,2,3] # 0次元1番・1次元2番・2次元3番の要素（0始まり）
t[:,1:-1,1:3] # コロン単体は全要素、-1 は最後の要素
t[1,2,3] = -10
t # インデックス/スライスの代入は元のテンソルを変更
t > 0 # 0 より大きい要素のマスク
t[t>0] # マスクで要素を選択（結果はベクトル）
